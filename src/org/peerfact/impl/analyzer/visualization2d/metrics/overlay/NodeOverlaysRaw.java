package org.peerfact.impl.analyzer.visualization2d.metrics.overlay;

import java.awt.Color;

/**
 * Name of the overlay / overlays, used by the node. (Use node attribute
 * "overlay")
 * 
 * @author Leo Nobach <peerfact@kom.tu-darmstadt.de>
 * @author Kalman Graffi <info@peerfact.org>
 * 
 * @version 08/18/2011
 * 
 */
public class NodeOverlaysRaw extends NodeOverlays {

	public NodeOverlaysRaw() {
		this.setColor(new Color(150, 200, 0));
	}

	@Override
	protected String getAttrKeyName() {
		return "overlay_raw";
	}

	@Override
	public String getName() {
		return "Overlays, class names";
	}

}
